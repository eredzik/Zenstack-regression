/** Expression for the client that performs the call (inside generated `run`). */
function emitInnerClientRef(dbAlias: string): string {
  // `this.db` / `this.prisma` in app code — harness passes the same client as `db`.
  if (dbAlias.startsWith("this.")) {
    return "db";
  }
  if (dbAlias === "prisma") return "db";
  return dbAlias;
}

function needsTransactionWrapper(
  dbAlias: string,
  transactionAliases: Set<string>
): boolean {
  if (dbAlias.startsWith("this.")) return false;
  if (dbAlias === "db" || dbAlias === "prisma") return false;
  return transactionAliases.has(dbAlias);
}

function buildRunnerBody(q: ExtractedQuery, transactionAliases: Set<string>): string {
  const inner = emitInnerClientRef(q.dbAlias);
  const args = q.argsSource.trim();
  const hasArgs = args.length > 0;

  const call = hasArgs
    ? `${inner}.${q.model}.${q.method}(${args})`
    : `${inner}.${q.model}.${q.method}()`;

  if (needsTransactionWrapper(q.dbAlias, transactionAliases)) {
    const param = q.dbAlias;
    // PrismaClient.$transaction is not on ZenstackCompareDb's index signature; cast for generated harness.
    return `return (db as unknown as { $transaction: (fn: (${param}: ZenstackCompareDb) => Promise<unknown>) => Promise<unknown> }).$transaction(async (${param}) => ${call});`;
  }
  return `return ${call};`;
}

function emitParamType(q: ExtractedQuery): string {
  if (q.params.length === 0) return "Record<string, never>";
  const lines = q.params.map((p) => `  ${JSON.stringify(p.name)}: ${p.typeText || "unknown"};`);
  return `{\n${lines.join("\n")}\n}`;
}

function emitParamDestructure(q: ExtractedQuery): string {
  if (q.params.length === 0) return "";
  return `const { ${q.params.map((p) => p.name).join(", ")} } = params;`;
}

function generateQueryFile(
  q: ExtractedQuery,
  opts: { headerComment?: string; transactionAliases?: string[] } = {}
): string {
  const txAliases = new Set(opts.transactionAliases ?? ["tx"]);
  const header =
    opts.headerComment ??
    "// Auto-generated by zenstack-query-compare — do not edit by hand.";
  const meta = {
    id: q.id,
    file: q.file,
    line: q.line,
    column: q.column,
    dbAlias: q.dbAlias,
    model: q.model,
    method: q.method,
  };
  const body = buildRunnerBody(q, txAliases);

  return `${header}

import { type QueryMeta, type ZenstackCompareDb } from "./_shared.js";

export const queryMeta: QueryMeta = ${JSON.stringify(meta, null, 2)};

export type QueryParams = ${emitParamType(q)};

export default async function runQuery(
  db: ZenstackCompareDb,
  params: QueryParams
): Promise<unknown> {
  ${emitParamDestructure(q)}
  ${body}
}
`;
}

export function writeManifestJson(
  outDir: string,
  manifest: ExtractManifest
): void {
  fs.mkdirSync(outDir, { recursive: true });
  const p = path.join(outDir, "extract-manifest.json");
  fs.writeFileSync(p, JSON.stringify(manifest, null, 2), "utf8");
}

